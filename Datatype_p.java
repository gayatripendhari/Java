public class Datatype_p {
    public static void main(String[] args) {
        float a = 10.0f;
        float b = 20.0f;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
    
}
